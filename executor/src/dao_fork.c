#define DAO_DRAIN_COUNT (sizeof(DAO_DRAIN_LIST) / sizeof(DAO_DRAIN_LIST[0]))

void apply_dao_fork(evm_state_t *state) {
    address_t refund_addr;
    memcpy(refund_addr.bytes, DAO_REFUND_ADDR, 20);

    /* Ensure the refund contract exists */
    if (!evm_state_exists(state, &refund_addr)) {
        evm_state_create_account(state, &refund_addr);
    }

    /* Drain each DAO account into the refund contract */
    for (size_t i = 0; i < DAO_DRAIN_COUNT; i++) {
        address_t drain_addr;
        memcpy(drain_addr.bytes, DAO_DRAIN_LIST[i], 20);

        uint256_t balance = evm_state_get_balance(state, &drain_addr);
        if (!uint256_is_zero(&balance)) {
            evm_state_sub_balance(state, &drain_addr, &balance);
            evm_state_add_balance(state, &refund_addr, &balance);
        }
    }
}
